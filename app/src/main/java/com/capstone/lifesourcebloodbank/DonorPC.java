package com.capstone.lifesourcebloodbank;

public class DonorPC {
    private String donorBloodGroup;
    private String donorAddress;
    private String donorPhoneNumber;
    private String donorUserName;
    private String donorName;
    private String donorAge;
    private String donorPincode;
    private String donorImage;

    public String getDonorBloodGroup() {
        return donorBloodGroup;
    }

    public void setDonorBloodGroup(String donorBloodGroup) {
        this.donorBloodGroup = donorBloodGroup;
    }

    public String getDonorAddress() {
        return donorAddress;
    }

    public void setDonorAddress(String donorAddress) {
        this.donorAddress = donorAddress;
    }

    public String getDonorPhoneNumber() {
        return donorPhoneNumber;
    }

    public void setDonorPhoneNumber(String donorPhoneNumber) {
        this.donorPhoneNumber = donorPhoneNumber;
    }

    public String getDonorUserName() { return donorUserName; }

    public void setDonorUserName(String donorUserName) { this.donorUserName = donorUserName; }

    public String getDonorName() { return donorName; }

    public void setDonorName(String donorName) { this.donorName = donorName; }

    public String getDonorAge() {
        return donorAge;
    }

    public void setDonorAge(String donorAge) {
        this.donorAge = donorAge;
    }

    public String getDonorPincode() {
        return donorPincode;
    }

    public void setDonorPincode(String donorPincode) {
        this.donorPincode = donorPincode;
    }
    public String getDonorImage() {
        return donorImage;
    }

    public void setDonorImage(String donorImage) {
        this.donorImage = donorImage;
    }

}
